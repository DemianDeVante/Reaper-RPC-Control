render_time = 1 -- protection time in seconds to let the items glue
savecursor =  reaper.GetCursorPosition() 
reaper.Main_OnCommand(40289, 0) -- unselect all items
explorerHWND = reaper.OpenMediaExplorer("", false)
reaper.JS_Window_OnCommand(explorerHWND, 41000) -- insert selected media file
starttime = reaper.time_precise()
doneglue = 0
donesource = 0

function main()
  if ( reaper.time_precise() - starttime >0.1) and doneglue == 0 then -------------- glue items
    item_count = reaper.CountSelectedMediaItems( 0 )
    if item_count > 0 then
      reaper.Main_OnCommand(40362, 0) -- glue items, ignoring time selection
      reaper.SetEditCurPos( savecursor, 1, 0 )
      doneglue = 1
      reaper.defer (main)
    end
  elseif ( reaper.time_precise() - starttime > render_time) and doneglue == 1 then --------------- undo and show path
      reaper.Main_OnCommand(41623, 0) -- open source in explorer
      reaper.Main_OnCommand(40029, 0) -- undo
      reaper.Main_OnCommand(40029, 0) -- undo
  else
    reaper.defer (main)
  end
end

main()
